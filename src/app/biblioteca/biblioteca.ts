/**
* This class represents an biblioteca of the Biblioteca. 
* It contains all the information relevant to the biblioteca.
*/
export class  Biblioteca {
   /**
    * Se refiere al identificador de cada biblioteca
    */
    id: number;
       
    
    /**
     * nombre de la biblioteca 
     */
    nombre: String;
      
        
    /**
     * Se refiere a la zona donde se encuentra la biblioteca
     */
    zona: String;
        
    /**
     * direccion de la biblioteca
     */
    direccion: String;
    
    /**
     * hora en que abre la biblioteca
     */
    horaApertura: String;
    
    /**
     * hora en que cierra la biblioteca
     */
    horaCierre: String ;

}
